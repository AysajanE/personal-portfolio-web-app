/* eslint-disable no-console */
const fs = require('fs')
const path = require('path')
const matter = require('gray-matter')

function isNonEmptyString(value) {
  return typeof value === 'string' && value.trim().length > 0
}

function main() {
  const repoRoot = process.cwd()
  const postsDir = path.join(repoRoot, 'content', 'posts')
  const outFile = path.join(repoRoot, 'lib', 'ogTitleMap.ts')

  if (!fs.existsSync(postsDir)) {
    console.error(`Missing posts directory: ${postsDir}`)
    process.exitCode = 1
    return
  }

  const files = fs.readdirSync(postsDir).filter(f => f.endsWith('.mdx'))
  const map = {}

  for (const file of files) {
    const slug = file.replace(/\.mdx$/, '')
    const filePath = path.join(postsDir, file)
    const raw = fs.readFileSync(filePath, 'utf8')
    const { data } = matter(raw)
    const title = isNonEmptyString(data?.title) ? data.title.trim() : slug
    map[slug] = title
  }

  const entries = Object.entries(map).sort(([a], [b]) => a.localeCompare(b))
  const lines = entries.map(([slug, title]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(title)},`)

  const contents = [
    '/* This file is generated by `scripts/generate-og-title-map.js`. */',
    'export const ogTitleMap: Record<string, string> = {',
    ...lines,
    '}',
    '',
  ].join('\n')

  fs.mkdirSync(path.dirname(outFile), { recursive: true })
  fs.writeFileSync(outFile, contents, 'utf8')

  console.log(`Wrote ${entries.length} titles to ${path.relative(repoRoot, outFile)}`)
}

main()
